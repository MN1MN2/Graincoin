    struct Checkpoint
    {
        uint256 hashCheckPoint;
        unsigned int nTime;
    };

    typedef std::map<int, Checkpoint> MapCheckpoints;

    Checkpoint initCheckpoint(uint256 hashCheckPoint, unsigned int nTime)
    {
        return (struct Checkpoint){hashCheckPoint, nTime};
    }

    //
